function inferLineItemIcon(id: string): LucideIcon {
  if (id.startsWith("income_")) {
    if (id.includes("tips") || id.includes("bonus")) return Gift;
    if (id.includes("app") || id.includes("platform")) return Smartphone;
    if (id.includes("retail") || id.includes("sale")) return Store;
    return DEFAULT_INCOME_ICON;
  }
  if (id.includes("fuel") || id.includes("energy")) return Fuel;
  if (id.includes("vehicle") || id.includes("van_") || id.includes("car_")) return Car;
  if (id.includes("insurance") || id.includes("liability") || id.includes("indemnity")) return Shield;
  if (id.includes("phone") || id.includes("data") || id.includes("internet")) return Smartphone;
  if (id.includes("bank") || id.includes("card_fees") || id.includes("fees")) return Landmark;
  if (id.includes("marketing") || id.includes("advert")) return Megaphone;
  if (id.includes("training") || id.includes("cpd")) return GraduationCap;
  if (id.includes("software") || id.includes("online")) return Laptop;
  if (id.includes("material") || id.includes("stock")) return Package;
  if (id.includes("tool")) return Wrench;
  if (id.includes("account")) return Calculator;
  if (id.includes("office") || id.includes("stationery")) return FileText;
  if (id.includes("rent") || id.includes("lease") || id.includes("chair")) return Building2;
  if (id.includes("waste") || id.includes("disposal")) return Trash2;
  return DEFAULT_EXPENSE_ICON;
}

export function getLineItemIcon(lineId: string): LucideIcon {
  return LINE_ITEM_ICONS[lineId] ?? inferLineItemIcon(lineId);
}

export function getTemplateIcon(templateId: string): LucideIcon {
  return TEMPLATE_ICONS[templateId] ?? Briefcase;
}
